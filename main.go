// Define the location type struct which contains the latitude and longitude of the location.
type Location struct {
	Lat float64 `json:"lat"`
	Lon float64 `json:"lon"`
}

// Define the post struct that contains the following attributes.
type Post struct {
	// `json:"user"` is for the json parsing of this User field. Otherwise, by default it's 'User'.
	User     string   `json:"user"`     // user name
	Message  string   `json:"message"`  // message of the post
	Location Location `json:"location"` // location of the post
	Url      string   `json:"url"`      // url of the image or video
	Type     string   `json:"type"`     // type of the file
	Face     float32  `json:"face"`     // face confidence value
}

